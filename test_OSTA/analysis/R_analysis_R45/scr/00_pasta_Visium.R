# explanation -------------------------------------------------------------
# Statistics
# Ii: This is the exact Local Moran's $I_i$ value that our loop calculated for that specific spot. A positive value means the spot is similar to its neighbors; a negative value means it is a spatial outlier (different from its neighbors). It is expected to not be bound between -1 and 1. It should be contextualized with the p value associated to identify significant correlation values.

# E.Ii: The Expected $I_i$ value if there were absolutely no spatial correlation (usually a number very close to zero).

# Var.Ii: The Variance of the expected $I_i$ value under the null hypothesis.

# Z.Ii: The Z-score. This represents how many standard deviations your observed $I_i$ is away from the expected $I_i$. Higher absolute Z-scores mean stronger evidence of a non-random spatial pattern.

# Pr(z != E(Ii)): The raw p-value. If this is very low (e.g., < 0.05), the spatial pattern around that specific spot is statistically significant.

# The Spatial Classifications (LISA Quadrants)
# The columns mean, median, and pysal are where the algorithm categorizes each spot into a spatial "quadrant." The pysal column explicitly mirrors the categorization logic from the popular Python spatial library of the same name.High-High: A "hot spot." This spot has high Myh2 expression AND is surrounded by neighbors with high expression.Low-Low: A "cold spot." This spot has low expression AND is surrounded by neighbors with low expression.High-Low / Low-High: These are "spatial outliers." For example, High-Low means a spot has high expression but is entirely surrounded by neighbors with low expression.

