# Multivariate measures – Bivariate Moran’s I -----------------------------
# There exists a bivariate version of Moran’s I as well.

# For two continuous observations the GLOBAL bivariate Moran’s I is defined as (Wartenberg 1985; Bivand 2022)

# Given variables (assuming they are already mean-centered):
# a: vector of expression counts for Gene A (e.g., Myh2)
# b: vector of expression counts for Gene B (e.g., Calr)
# W: spatial weight matrix
# n: total number of spots (length(a))

# ---------------------------------------------------------
# 1. The Denominator: sum_{i} (b[i]^2)
# ---------------------------------------------------------
# denominator_sum <- 0
# for (i in 1:n) {
#   denominator_sum <- denominator_sum + (b[i]^2)
# }
# ---------------------------------------------------------
# 2. The Numerator: sum_{i} ( sum_{j} (W[i,j] * b[j]) * a[i] )
# ---------------------------------------------------------
# numerator_sum <- 0
# 
# for (i in 1:n) {
#   
#   # Inner loop: calculate the spatial lag of 'b' for target spot 'i'
#   spatial_lag_b <- 0
#   for (j in 1:n) {
#     spatial_lag_b <- spatial_lag_b + (W[i, j] * b[j])
#   }
#   
#   # Multiply spot i's 'a' value by the spatial lag of 'b', and add to numerator sum
#   numerator_sum <- numerator_sum + (spatial_lag_b * a[i])
# }
# ---------------------------------------------------------
# 3. Final Bivariate Moran's I
# ---------------------------------------------------------
# I_B <- numerator_sum / denominator_sum

# This formula calculates the GLOBAL Bivariate Moran's I (I_B).
# It measures the spatial correlation between two DIFFERENT features (e.g., Gene A and Gene B).
#
# 1. The inner summation `sum_j (w[i,j] * b[j])`:
# This calculates the "spatial lag" for feature 'b' around our target spot 'i'. It iterates through all spots 'j', multiplying the spatial weight by the value of 'b'.
#
# 2. The numerator `sum_i ( [spatial_lag_b] * a[i] )`:
# It iterates through all target spots 'i', multiplying the value of feature 'a' at spot 'i' by the spatial lag of feature 'b' we just calculated, summing them all up.
#
# 3. The denominator `sum_i (b[i]^2)`:
# This represents the total variance of feature 'b'. It iterates through all spots,squaring the value of 'b' and adding it to a running total.

# where `ai` and `bi`are the two variables of interest and `wij` is the value of the spatial weights matrix for positions `i` and `j`. It is a measure of the correlation of the variables `a` with the average of the neighboring values for variable `b` (also called the spatial lag of `b`).
