# explanation -------------------------------------------------------------
# This formula calculates Local Bivariate Moran's I (I_B,i) for a specific spot `i`.
# It measures if Gene A at spot `i` is correlated with Gene B in its surrounding neighbors.
#
# n <- length(z_A)

# ---------------------------------------------------------
# 1. The Spatial Lag: sum_{j=1}^n (W[i, j] * z_B[j])
# ---------------------------------------------------------
# spatial_lag_B <- 0
# 
# for (j in 1:n) {
#   # Multiply the neighbor's Gene B value by the spatial weight
#   spatial_lag_B <- spatial_lag_B + (W[i, j] * z_B[j])
# }
# ---------------------------------------------------------
# 2. Final Local Bivariate Moran's I for spot 'i'
# ---------------------------------------------------------
# Multiply target spot's Gene A value by the surrounding neighborhood's Gene B value
# I_Bi <- z_A[i] * spatial_lag_B

# Mathematical components:
# 1. `z_A[i]`: The standardized value (Z-score) of Gene A at our target spot `i`.
# 2. The summation `sum_{j=1}^n w[i,j] * z_B[j]`: This is the spatial lag of Gene B.
#    It iterates through all spots `j`, multiplying the spatial weight by the 
#    standardized value of Gene B, effectively calculating the neighborhood average.
#
# Assumptions:
# z_A and z_B are already standardized vectors (mean = 0, variance = 1).
# For example, in R you would do: z_A <- scale(Myh2); z_B <- scale(Calr)

# why the metric is asymmetric?
# To understand why this asymmetry is actually a massive advantage for spatial transcriptomics, imagine a tumor microenvironment:
# Gene A is a tumor marker.Gene B is an immune cell marker (like a T-cell).
# Imagine a single tumor cell (high Gene A, low Gene B) completely surrounded by a ring of attacking immune cells (low Gene A, high Gene B). Let's look at the target spot $i$ (the tumor cell) from both directions:
# Direction 1 ($A \to B$): The center spot has high Gene A. The neighbors have high Gene B.Result: High-High cluster. (Interpretation: "This tumor cell is surrounded by immune cells").
# Direction 2 ($B \to A$): The exact same center spot has low Gene B (it is a tumor cell, not an immune cell). The neighbors have low Gene A (they are immune cells, not tumor cells).Result: Low-Low cluster. (Interpretation: "This spot lacks immune cells, and its neighbors lack tumor cells").
# By being asymmetric, the local bivariate metric precisely captures the biological architecture—specifically, which cell type is sitting at the core and which cell type makes up the surrounding shell .
