# Impact of neighbourhood on autocorrelation measures ---------------------
# Let’s compare the impact of different spatial weight matrices on local autocorrelation analysis. In this Visium dataset we do not have gene expression information for each cell because of the resolution of the spots. We will instead calculate the autocorrelation measures on the area of the segmented cells. This could be helpful when looking at local cell densities in a tissue.

# First, we base the spatial weight matrix on contiguous neighbours.
# The annotGeometryUnivariate() function works identically to the runUnivariate() function you used earlier, but instead of applying the math to the circular Visium spots (the regular lattice), it applies it to the annotated geometries—in this case, the myofiber_simplified polygons.

# area is a metric assigned to each individual unit defined in your network. Think of it like a three-layer cake:
# The Geometry (myofiber_simplified): This is the base layer. It is a collection of irregular, physically drawn polygons representing the segmented muscle cells.
# The Metric (area): This is the data attached to those shapes. It is simply the 2D physical footprint (the size) of each specific polygon.
# The Graph (myofiber_poly2nb): This is the network layered on top. Because these are irregular polygons (unlike the perfect grid of Visium spots), this graph calculates exactly which muscle fibers share a border and are physically touching one another.

sfe_tissue <- annotGeometryUnivariate(sfe_tissue,
                                      type = "localmoran",
                                      features = "area",
                                      annotGeometryName = "myofiber_simplified",
                                      annotGraphName = "myofiber_poly2nb",
                                      include_self = FALSE, zero.policy = TRUE,
                                      name = "myofiber_poly2nb")

# extract the values
myofibers <- annotGeometry(sfe_tissue, "myofiber_simplified")
