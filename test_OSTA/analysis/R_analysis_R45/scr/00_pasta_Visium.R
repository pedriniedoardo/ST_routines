# explain how it works ----------------------------------------------------
# Here is a step-by-step breakdown of exactly what type = "moran.mc" (Monte Carlo permutation for Moran's I) is doing under the hood:
# 1. Calculate the Observed Moran's I: 
# First, the algorithm calculates the actual Moran's I using your real data. It looks at the real gene expression of Myh2 at Spot 1, compares it to its real neighbors, and gets a baseline score. Let's say this observed score is 0.65 (indicating strong positive spatial correlation).
                                                                  
# 2. The Permutation (The "Shuffle"):
# Next, the algorithm takes all the observed gene expression values for Myh2. It leaves the empty spots (and their defined neighbor connections) exactly where they are on the lattice.
# Then, it randomly pulls values of expression and randomly assign them back onto the tissue slide.
# Because the values are assigned randomly, any biological "clumping" or spatial pattern is completely destroyed, but the overall distribution of the gene (the mean, the variance) remains identical.
# 3. Calculate the "Pseudo" Moran's I:
# Now, it recalculates Moran's I on this newly randomized tissue map. Because the data is randomly scattered, the Moran's I score should drop close to 0 (no spatial correlation).
# 4. Repeat nsim Times:
# The algorithm will shuffle the values and recalculate the "pseudo" Moran's I 100 different times. This builds a "null distribution".
# 5. Calculate the P-Value (Confidence):
# Finally, it compares your original, real Moran's I (0.65) to the 100 random scores.
# If the real score of 0.65 is higher than all 100 random scores, the p-value is < 0.01. The spatial clustering you see is almost impossible to achieve by random chance. Else, the observed pattern could easily just be noise.
# -------------------------------------------------------------------------

# Local Measures for Univariate Data --------------------------------------
# Often, a global measure is not enough. One number determining e.g. the spatial autocorrelation over an entire tissue slice might not be reflective of tissue heterogeneity. Therefore, local indicators of spatial associations have been developed (Pebesma and Bivand 2023; Anselin 1995).

# Local Moran’s I ---------------------------------------------------------
# Local Moran’s I provides a measure of spatial autocorrelation at each location, highlighting local clusters of similarity or dissimilarity (Anselin 1995; Pebesma and Bivand 2023). It is defined as:

# Given variables:
# x: vector of gene expression counts
# W: spatial weight matrix
# i: the specific spot index we are calculating the score for
# n: total number of spots (length(x))

# x_bar <- mean(x)
# n <- length(x)

# ---------------------------------------------------------
# 1. The Denominator: sum_{k=1}^n (x[k] - x_bar)^2 / (n - 1)
# ---------------------------------------------------------
# var_sum <- 0
# for (k in 1:n) {
#   var_sum <- var_sum + (x[k] - x_bar)^2
# }
# denominator <- var_sum / (n - 1)

# ---------------------------------------------------------
# 2. The Spatial Lag: sum_{j=1}^n W[i, j] * (x[j] - x_bar)
# ---------------------------------------------------------
# spatial_lag_sum <- 0
# for (j in 1:n) {
#   spatial_lag_sum <- spatial_lag_sum + (W[i, j] * (x[j] - x_bar))
# }

# ---------------------------------------------------------
# 3. Final Local Moran's I for spot 'i'
# ---------------------------------------------------------
# I_i <- ((x[i] - x_bar) / denominator) * spatial_lag_sum
  
# This formula calculates Local Moran's I (I_i) for a specific spot `i`. It measures the local spatial autocorrelation to identify local clusters or outliers.
# 1. `x[i] - x_bar`: 
# The deviation of our target spot `i` from the global mean (`x_bar`).
#
# 2. The denominator `sum_{k=1}^n (x[k] - x_bar)^2 / (n - 1)`:
# This represents the sample variance. In loop notation, it requires iterating an index `k` from 1 to `n` (across all spots in the dataset), squaring each spot's deviation from the mean, summing them, and dividing by `n - 1`.
#
# 3. The spatial lag `sum_{j=1}^n W[i, j] * (x[j] - x_bar)`:
# This represents the spatially weighted neighborhood sum. It requires iterating an index `j` from 1 to `n`. For every spot `j`, we multiply the spatial weight `W[i, j]` by spot `j`'s deviation from the mean, and add it to a running total. (If `i` and `j` are not neighbors, `W[i, j]` is 0, canceling that term out).
#
# Conceptual calculation for a single spot `i`:
# var_sum <- 0; for(k in 1:n) { var_sum <- var_sum + (x[k] - x_bar)^2 }
# lag_sum <- 0; for(j in 1:n) { lag_sum <- lag_sum + (W[i, j] * (x[j] - x_bar)) }
# I_i <- ((x[i] - x_bar) / (var_sum / (n - 1))) * lag_sum
  
# where the index `i` refers to the location for which the measure is calculated. The interpretation is analogous to global Moran’s I where a value of `I_i` higher than E(I_i) = -w[i]/(n-1) indicates spatial autocorrelation; smaller values indicate negative autocorrelation (Anselin 1995). It is important to note that, as for the global counterpart, the value of local Moran’s I could be a result from both the high or low end of the values. Here we will calculate the local Moran’s I value for the measurement of muscle fiber marker gene Myh2:
