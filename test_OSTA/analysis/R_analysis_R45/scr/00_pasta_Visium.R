# Spatial autocorrelation -------------------------------------------------
# Spatial autocorrelation measures the association between spatial units while considering that these spatial units are not independent measurements. These measures can be global (summarizing the entire field) or local (providing statistics for individual locations) (Pebesma and Bivand 2023).

# Global Measures ---------------------------------------------------------
# Global methods give us an overview over the entire field-of-view and summarize the spatial autocorrelation metric to a single value. The metrics are a function of the weight matrix and the variables of interest. The variables of interest can be e.g. gene expression, intensity of a marker or the area of a cell. The global measures can be seen as a weighted average of the local metric, as explained below.

# In general, a global spatial autocorrelation measure has the form of a double sum over all locations 

# n <- length(x)
# total_sum <- 0
# 
# # The double summation translates to a double loop
# for (i in 1:n) {
#   for (j in 1:n) {
#     # Multiply the function output by the spatial weight
#     total_sum <- total_sum + ( f(x[i], x[j]) * W[i, j] )
#   }
# }

# `i` and `j` represent pairs of spots across the tissue lattice.
# x[i] and x[j] are the metrics measured at those spots (e.g., gene expression counts).
# where `f(x[i], x[j])` is the measure of association between features of interest (these changes base on the metric used, for example Morons'I or Geary's C) and `W[i, j]` scales the relationship by a spatial weight as defined in the weight matrix `W` (Zuur, Ieno, and Smith 2007; Pebesma and Bivand 2023).
# If you are calculating Moran's I (measuring global similarity), `f(x[i], x[j])` is defined as the cross-product of their deviations from the mean: `(x[i] - avg(x))(x[j] - avg(x))`.
# If you are calculating Geary's C (measuring spatial dissimilarity), `f(x[i], x[j])` is the squared difference between the two values: `(x[i] - x[j])^2`.

# Moran’s I ---------------------------------------------------------------
# is the most prominent measure of spatial autocorrelation. The values are bounded by -1 and 1. The expected value is close to 0 for large `n`, the exact value is given by `E(I) = -1/(n-1)`. A value higher than the expected value indicates spatial autocorrelation. Negative values indicate negative autocorrelation.
# Spatial autocorrelation means that similar values tend to be found together in the tissue. In fact, Moran’s I can be interpreted as the Pearson correlation between the value at location `i` and the averages value of the neigbours of `i`, (neighbours as defined in the weight matrix `W`) (Moran 1950; Cliff and Ord 1981, 21).

