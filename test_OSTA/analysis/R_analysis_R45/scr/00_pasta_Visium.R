# explanation -------------------------------------------------------------
# what is the Inherent (Non-Spatial) Correlation?
# Imagine you are looking at two genes, Gene X and Gene Y. Biologically, they are tightly co-expressed. Whenever a cell turns on Gene X, it automatically turns on Gene Y.
# This is an inherent, non-spatial correlation. They are correlated simply because of the cell's internal biology, entirely regardless of where that cell is physically located on your tissue slide. If you ran a standard Pearson correlation on these two genes, the score would be very high.

# The Global Statistic Method
